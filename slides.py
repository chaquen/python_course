numeros=['primero','segundo','tercero',4,'quinto','6','septimo','noveno','decimo',11]
print("Lista de numeros")
print(numeros)
print("Slides son tajadas del pastel es decir de la lista")
print("La tajada completa")
print(numeros[:])
print("La tajada desde el segundo elemento hasta el último")
print(numeros[1:])
print("La tajada desde el cuarto al sexto elemento hasta el último")
print(numeros[3:6])
print("La tajada desde el sexto elemento  hasta el penúltimo, indice negativo")
print(numeros[-5:-1])
print("La tajada desde el  primer elemento  hasta el quinto elemento")
print(numeros[:5])



